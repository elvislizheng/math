export function getStrand(id: string): Strand | undefined {
  return strands.find((s) => s.id === id);
}

export function getSubstrand(
  strandId: string,
  subId: string
): { strand: Strand; substrand: typeof strands[0]["substrands"][0] } | undefined {
  const strand = getStrand(strandId);
  if (!strand) return undefined;
  const substrand = strand.substrands.find((s) => s.id === subId);
  if (!substrand) return undefined;
  return { strand, substrand };
}

export function getExpectation(code: string) {
  for (const strand of strands) {
    for (const sub of strand.substrands) {
      const exp = sub.expectations.find((e) => e.code === code);
      if (exp) return { strand, substrand: sub, expectation: exp };
    }
  }
  return undefined;
}
