// Helper function to get questions by strand
export function getQuestionsByStrand(strand: string): Question[] {
  return questions.filter((q) => q.strand === strand);
}

// Helper function to get random questions for a dungeon
export function getRandomQuestions(strand: string, count: number): Question[] {
  const strandQuestions = getQuestionsByStrand(strand);
  const shuffled = [...strandQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}
